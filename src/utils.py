def json_serial(obj):
    """JSON serializer for objects not serializable by default json code"""

    if isinstance(obj, (datetime, date)):
        return obj.isoformat()
    raise TypeError ("Type %s not serializable" % type(obj))


def request(res):
    uri = Configuration()['proxy-cli.base-addr'] + res
    return urllib.request.urlopen(uri).read()


def post(res, body):
    h = urllib.request.Request(
        Configuration()['proxy-cli.base-addr'] + res,
        data = json.dumps(str(body)).encode(),
        headers={'Content-Type': 'application/json'}
    )
    return urllib.request.urlopen(h).read()

